static int fs_stat (const char *filename, vfs_stat_t *st)
{
    const embedded_file_t *file;

    if((file = find_file(filename))) {
        memset(st, 0, sizeof(vfs_stat_t));
        st->st_size = file->size;
        st->st_mode.read_only = true;
    }

    return file ? 0 : -1;
}


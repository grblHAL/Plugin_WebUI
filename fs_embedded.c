{
}

static int fs_stat (const char *filename, vfs_stat_t *st)
{
    const embedded_file_t *file;

    if((file = find_file(filename))) {
        memset(st, 0, sizeof(vfs_stat_t));
        st->st_size = file->size;
        st->st_mode.read_only = true;
    }

    return file ? 0 : -1;
}

void fs_embedded_mount (void)
{
    static const vfs_t fs = {
        .mode.directory = true,
        .mode.hidden = true,
        .mode.read_only = true,
        .fopen = fs_open,
        .fclose = fs_close,
        .fread = fs_read,
        .fwrite = fs_write,
        .ftell = fs_tell,
        .feof = fs_eof,
        .funlink = fs_unlink,
        .fmkdir = fs_dirop,
        .fchdir = fs_dirop,
        .frmdir = fs_dirop,
        .fopendir = fs_opendir,
        .fclosedir = fs_closedir,
        .fstat = fs_stat
    };

    vfs_mount("/embedded", &fs);
}

